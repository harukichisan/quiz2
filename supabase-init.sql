-- クイズ問題テーブルの作成
CREATE TABLE IF NOT EXISTS questions (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  question TEXT NOT NULL,
  answer TEXT NOT NULL,
  category TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

ALTER TABLE questions
  ADD COLUMN IF NOT EXISTS difficulty TEXT;

UPDATE questions
  SET difficulty = 'C'
  WHERE difficulty IS NULL;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_constraint
    WHERE conrelid = 'questions'::regclass
      AND conname = 'questions_difficulty_check'
  ) THEN
    ALTER TABLE questions
      ADD CONSTRAINT questions_difficulty_check CHECK (difficulty IN ('C','B','A','S'));
  END IF;
END
$$;

ALTER TABLE questions
  ALTER COLUMN difficulty SET NOT NULL;

-- カテゴリ別検索用のインデックス
CREATE INDEX IF NOT EXISTS idx_questions_category ON questions(category);

CREATE UNIQUE INDEX IF NOT EXISTS idx_questions_question_unique ON questions(question);

-- Row Level Security (RLS) の有効化
ALTER TABLE questions ENABLE ROW LEVEL SECURITY;

-- 全ユーザーが問題を読み取れるポリシー
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_policies
    WHERE schemaname = current_schema()
      AND tablename = 'questions'
      AND policyname = '全ユーザーが問題を閲覧可能'
  ) THEN
    CREATE POLICY "全ユーザーが問題を閲覧可能" ON questions
      FOR SELECT
      USING (true);
  END IF;
END
$$;

